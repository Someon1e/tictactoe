    pub fn read_input(&mut self) -> Option<(u8, u8)> {
        self.stdin.read_line(&mut self.input).unwrap();

        let mut chars = self.input.chars();

        let column = match chars.nth(0) {
            Some('a') => 0,
            Some('b') => 1,
            Some('c') => 2,
            _ => {
                self.input.clear();
                return None;
            }
        };

        let row = match chars.nth(0) {
            Some('1') => 0,
            Some('2') => 1,
            Some('3') => 2,
            _ => {
                self.input.clear();
                return None;
            }
        };

        self.input.clear();
        Some((column, row))
    }
